CREATE TABLE [dbo].[Characters]
(
	[Id] INT NOT NULL PRIMARY KEY IDENTITY, 
    [Name] NVARCHAR(50) NOT NULL, 
    [TypeId] INT NOT NULL, 
    [SpeciesId] INT NOT NULL, 
    [GenderId] INT NOT NULL, 
    [SideId] INT NOT NULL, 
    [Tier2] BIT NOT NULL DEFAULT 0, 
    [PhysicalAtk] INT NOT NULL DEFAULT 0, 
    [EnergyAtk] INT NOT NULL DEFAULT 0, 
    [AtkSpeed] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [CritRate] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [CritDmg] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [IgnoreDef] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [IgnoreDodge] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [FireDmg] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [ColdDmg] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [LightningDmg] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [PoisonDmg] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [MindDmg] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [PhysicalDef] INT NOT NULL DEFAULT 0, 
    [EnergyDef] INT NOT NULL DEFAULT 0, 
    [HP] INT NOT NULL DEFAULT 0, 
    [RecoveryRate] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [Dodge] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [FireResist] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [ColdResist] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [LightningResist] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [PoisonResist] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [MindResist] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [MoveSpd] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [RedDebuffDur] DECIMAL(4, 2) NOT NULL DEFAULT 0.00, 
    [RedCooldownDur] DECIMAL(4, 2) NOT NULL DEFAULT 0.00
)
